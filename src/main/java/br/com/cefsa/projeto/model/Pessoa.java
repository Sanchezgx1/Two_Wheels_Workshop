public class Pessoa {

    private Long id;
    private String usuaio;
    private String senha;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsuaio() {
        return usuaio;
    }

    public void setUsuaio(String usuaio) {
        this.usuaio = usuaio;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }


}
